//Construye un código bajo las siguientes reglas
// °Declara un arreglo vacío *Con un ciclo While que se ejecute 5 veces, agrega los elementos al array

var arreglo = []
var i=0

while(i<5){
    arreglo[i] = i
    i++
}

//FUNCIÓN CONSOLE.LOG PARA VERIFICAR QUE LOS NÚMEROS SE AGREGARON AL ARRAY
console.log("Contenido del arreglo:", arreglo)


